class 	StringSwitch
{
public static void main(String a[])
{
String s="Anusha";
switch(s)
{
   case "Anusha":
      System.out.println("Anusha");
      break;
   case "Yaram": 
      System.out.println("Yaram");
      break;
   default:
      System.out.print("invalid input");
}
}
}






