
class TwoDeminsionalArray
{
     public static void main(String[] args)
       {    
          int p[][]={
                          {6,3,4,6},
                          {7,8,9,5},
                          {2,5,7,6}
                     }; 
          for(int i=0;i<3;i++)
            {
               for(int j=0;j<4;j++)
                  {
                    System.out.print(p[i][j] + " ");
                  }
               System.out.println(" "); 
            }  
           
       } 
           
           
}